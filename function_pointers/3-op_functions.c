#include "3-calc.h"

/**
 * op_add - add two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: the multiplcation
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - add two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: the division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - add two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: the modulo
 */
int op_mod(int a, int b)
{
	return (a % b);
}
